# Condicional Elif

letra = "u"

if letra.lower() == "a":
    print("Esta vocal es A")

elif letra.lower() == "e":
    print("Esta vocal es E")

elif letra.lower() == "i":
    print("Esta vocal es I")

elif letra.lower() == "o":
    print("Esta vocal es O")

elif letra.lower() == "u":
    print("Esta vocal es U")

else:
    print("Error ingrese una vocal")